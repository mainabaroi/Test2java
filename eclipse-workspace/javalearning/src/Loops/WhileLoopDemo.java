package Loops;

public class WhileLoopDemo {

	public static void main(String[] args) {
		// While Loops - is a control flow statement that allows code to be execute repeatedly base on a given Boolean condition.

		int i=1;
		
		while (i<=10)
		{
			System.out.println(i);
			i++;      //++ is adding i upto 10. 
		}
		
		
		
		
		
		
		
		
		
		
	}

}
